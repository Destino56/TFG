package model;

public class Entity {

    private int id;
    private String name;
    private int health;
    private int dice;
    private int damage;
    private int dodge;
    private int precission;
    private String folder;
    private String sprite;
    private int widht;
    private int height;
    private int posX;
    private int posY;
    private int posXVida;
    private int posYVida;
    private int numSprites;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDice() {
        return dice;
    }

    public void setDice(int dice) {
        this.dice = dice;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getDodge() {
        return dodge;
    }

    public void setDodge(int dodge) {
        this.dodge = dodge;
    }

    public int getPrecission() {
        return precission;
    }

    public void setPrecission(int precission) {
        this.precission = precission;
    }

    public String getFolder() {
        return folder;
    }

    public void setFolder(String folder) {
        this.folder = folder;
    }

    public String getSprite() {
        return sprite;
    }

    public void setSprite(String sprite) {
        this.sprite = sprite;
    }

    public int getWidht() {
        return widht;
    }

    public void setWidht(int widht) {
        this.widht = widht;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

    public int getPosXVida() {
        return posXVida;
    }

    public void setPosXVida(int posXVida) {
        this.posXVida = posXVida;
    }

    public int getPosYVida() {
        return posYVida;
    }

    public void setPosYVida(int posYVida) {
        this.posYVida = posYVida;
    }

    public int getNumSprites() {
        return numSprites;
    }

    public void setNumSprites(int numSprites) {
        this.numSprites = numSprites;
    }
}
